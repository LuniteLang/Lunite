#[derive(Debug, Clone, PartialEq)]
pub enum TokenKind {
    Illegal,
    EOF,
    Identifier(String),
    IntLiteral(i64),
    FloatLiteral(f64),
    String(String),
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Equal,
    EqualEqual,
    Bang,
    BangEqual,
    Less,
    LessEqual,
    Greater,
    GreaterEqual,
    Arrow,
    FatArrow,
    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    Colon,
    ColonColon,
    Comma,
    Dot,
    DotDot,
    Ampersand,
    Pipe,
    Caret,
    LessLess,
    GreaterGreater,
    Question,
    Fn,
    Let,
    Return,
    If,
    Else,
    While,
    For,
    In,
    Struct,
    Enum,
    Impl,
    Use,
    Import,
    Extern,
    Native,
    Pub,
    Pure,
    Req,
    Layout,
    Soa,
    As,
    Try,
    Catch,
    Throw,
    True,
    False,
    Null,
    Mut,
    When,
    Region,
    Comptime,
    Spawn,
    Sizeof,
    Int,
    Float,
    Bool,
    StringType,
    Void,
    Newline,
    Semicolon,
}

#[derive(Debug, Clone)]
pub struct Token {
    pub kind: TokenKind,
    pub line: usize,
    pub column: usize,
    // pub span: Span, // Uncomment when Span is moved to a shared crate or module
}
